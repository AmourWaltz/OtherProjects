void oled_display_chinese(uint8 uc_posx, uint8 uc_posy, uint8 Chinese_str_num, enum OLED_LIGHT_MODE mode)
{
    uint8 i = 0, uc_dataTmp = 0;
	uint16 j = 0, start_char = 0, end_char = 0;

	switch (Chinese_str_num)
	{
		//华中科技大学
		case HUST_UNIVERSITY:
			start_char = 0;
			end_char = 5;
			break;
		//无线节能
		case SMARTCAR_WCES:
			start_char = 6;
			end_char = 9;
			break;
		//调参
		case SET_PARAM:
			start_char = 10;
			end_char = 11;
			break;
		//测试
		case HARDWARE_TEST:
			start_char = 12;
			end_char = 13;
			break;
		//充电
		case WIRELESS_CHARGE:
			start_char = 14;
			end_char = 15;
			break;
		//起飞
		case CAR_RUNNING:
			start_char = 16;
			end_char = 17;
			break;
                //薛博阳
                case XUE_BOYANG:
                        start_char = 18;
			end_char = 20;
			break;
                //止于至善
                case PERFECT_MAKING:
                        start_char = 21;
			end_char = 24;
			break;                        
		default:
			break;
	}

	if (mode == LOW_LIGHT_MODE)
	{
		for (j=start_char ; j<=end_char ; j++)
		{
			uc_dataTmp = j;

			oled_set_pos(uc_posx,uc_posy);

			for (i=0 ; i<16 ; i++)
			{
				oled_write_data(OLED_16X8GB2312[uc_dataTmp][i]);
			}

			oled_set_pos(uc_posx,uc_posy+1);

			for (i=16 ; i<32 ; i++)
			{
				oled_write_data(OLED_16X8GB2312[uc_dataTmp][i]);
			}

			uc_posx += 16;
		}
	}
	else if (mode == HIGH_LIGHT_MODE)
	{
		for (j=start_char ; j<=end_char ; j++)
		{
			uc_dataTmp = j;

			oled_set_pos(uc_posx,uc_posy);

			for (i=0 ; i<16 ; i++)
			{
				oled_write_data(~OLED_16X8GB2312[uc_dataTmp][i]);
			}

			oled_set_pos(uc_posx,uc_posy+1);

			for (i=16 ; i<32 ; i++)
			{
				oled_write_data(~OLED_16X8GB2312[uc_dataTmp][i]);
			}

			uc_posx += 16;
		}
	}
}

