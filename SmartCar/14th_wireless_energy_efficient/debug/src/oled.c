void oled_print_char_hl(uint8 uc_posx, uint8 uc_posy, int8 c_data)
{
    uint16 i, j, k, us_data;

    if(c_data < 0)
    {
    	oled_display_6x8char_hl(uc_posx, uc_posy, '-');
        us_data = (uint16)(-c_data);
    }
    else
    {
    	oled_display_6x8char_hl(uc_posx, uc_posy, '+');
        us_data = (uint16)c_data;
    }

    i =  us_data / 100;
    j = (us_data % 100) / 10;
    k =  us_data % 10;

    oled_display_6x8char_hl(uc_posx+6, uc_posy, i+48);
    oled_display_6x8char_hl(uc_posx+12, uc_posy, j+48);
    oled_display_6x8char_hl(uc_posx+18, uc_posy, k+48);

    return;
}
