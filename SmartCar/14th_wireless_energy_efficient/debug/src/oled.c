/**
 *    @brief   oled_pin_init
 */
void oled_pin_init(void)
{
    uint8 i;
    OCB *p_lgcb;

    for (p_lgcb = ocb_table, i = 0; i < OLED_NUM; p_lgcb++, i++)
    {
        p_lgcb->openflag  = false;
        p_lgcb->charrorflag = E_SYS;
    }

    /* 打开IO口 */

    initSCLK_DDR();
    OLED_SCLK(LOW);
    initSDIN_DDR();
    OLED_SDIN(LOW);
    initDC_DDR();
    OLED_DC(LOW);
    initRST_DDR();
    OLED_RST(LOW);
}




