/**
 *    @brief   写一个字节数据
 *
 *    @param   uint8 uc_data
 */
//低位在一竖的顶部 高位在底部
void oled_write_data(uint8 uc_data)
{
    uint8 i = 8;
    //gpio_set(OLED_PORT_GET(OLED_DC),OLED_PIN_GET(OLED_DC),1);       //DC = 1,write data
    //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),0);
    OLED_DC(HIGH);
    OLED_SCLK(LOW);

    while (i--)
    {
        if (uc_data & 0x80)
        {
        	//gpio_set(OLED_PORT_GET(OLED_SDIN),OLED_PIN_GET(OLED_SDIN),1);
        	OLED_SDIN(HIGH);
        }
        else
        {
        	//gpio_set(OLED_PORT_GET(OLED_SDIN),OLED_PIN_GET(OLED_SDIN),0);
        	OLED_SDIN(LOW);
        }
        /* 普通I/O口模拟时钟信号 */
        //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),1);
        OLED_SCLK(HIGH);
        asm("nop");
        //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),0);
        OLED_SCLK(LOW);

        uc_data <<= 1;
    }
    return;
}

/**
 *    @brief   写一个字节命令
 *
 *    @param   uint8 uc_cmd
 */
void  oled_write_command(uint8 uc_cmd)
{
    uint8 i = 8;
    //gpio_set(OLED_PORT_GET(OLED_DC),OLED_PIN_GET(OLED_DC),0);       //DC = 0,write command
    //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),0);
    OLED_DC(LOW);
    OLED_SCLK(LOW);

    while (i--)
    {
        if (uc_cmd & 0x80)
        {
        	//gpio_set(OLED_PORT_GET(OLED_SDIN),OLED_PIN_GET(OLED_SDIN),1);
            OLED_SDIN(HIGH);
        }
        else
        {
        	//gpio_set(OLED_PORT_GET(OLED_SDIN),OLED_PIN_GET(OLED_SDIN),0);
            OLED_SDIN(LOW);
        }
        /* 普通I/O口模拟时钟信号 */
        //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),1);
        OLED_SCLK(HIGH);
        asm("nop");
        //gpio_set(OLED_PORT_GET(OLED_SCLK),OLED_PIN_GET(OLED_SCLK),0);
        OLED_SCLK(LOW);
        uc_cmd <<= 1;
    }
    return;
}


